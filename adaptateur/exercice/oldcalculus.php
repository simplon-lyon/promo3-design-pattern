<?php

namespace adaptateur\exercice;


class OldCalculus{
    public function add($a, $b) {
        return $a+$b;
    }
    public function substract($a, $b) {
        return $a-$b;
    }
    public function multiply($a, $b) {
        return $a*$b;
    }
    public function divide($a, $b) {
        return $a/$b;
    }
}